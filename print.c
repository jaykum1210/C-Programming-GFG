#include <stdio.h>

int main(){
    printf("Hello World!\n");
    printf("Integer = %d\n", 10);
    printf("Integer = %i\n", 20);
    printf("Float = %0.2f\n",20.3565);
    printf("Long int = %ld\n",20552222);
    printf("long long = %lld\n", 345465454645);
    printf("Short int = %hd\n", 12235);
    printf("Double = %lf\n", 20.123456);
    printf("Long double = %Lf\n", 20.123456789);
    printf("Char = %c\n", 'a');
    printf("String = %s\n","Jay");
    return 0;
}